set -e

export TMOUT=0
umask 022

RED='\033[31m'
GREEN='\033[32m'
YELLOW='\033[33m'
RESET='\033[0m'

source /etc/profile

systemctl enable docker --now

dockerStatus=$(systemctl is-active docker)
if [ "$dockerStatus" == "active" ]; then
  echo -e "${GREEN}Docker Status Successfully ${RESET}"
else
  echo -e "${RED}docker systemd is not running ${RESET}"
  exit 1
fi
